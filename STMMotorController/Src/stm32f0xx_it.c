/**
  * @brief  This function handles external interrupt requests.
  * @param  None
  * @retval None
  */
void EXTI0_1_IRQHandler(void)
{
	// Clear the interrupt
	EXTI->PR |= EXTI_PR_PR0;
	
	// Do something else
	GPIOC->ODR ^= GPIO_ODR_8;
}


